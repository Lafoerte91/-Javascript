function structureYear(data) {
  let res = {}

  data.forEach(item => {
    const {year, month, day, data} = item
    // Инициализируем объект для года, если его еще нет
    if(!res[year]) {
      res[year] = {}
    }
    // Инициализируем объект для месяца, если его еще нет
    if(!res[year][month]) {
      res[year][month] = {}
    }
    // Инициализируем массив для дня, если его еще нет
    if(!res[year][month][day]) {
      res[year][month][day] = []
    }
    // Добавляем данные в массив для конкретного дня
    res[year][month][day].push(...data)
  })

  return res
}


